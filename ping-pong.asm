LIST P=16F628A
INCLUDE "P16F628A.INC"
    
__CONFIG _XT_OSC & _WDT_OFF & _PWRTE_ON & _BODEN_OFF & _LVP_OFF & _CP_OFF & _CPD_OFF &_MCLRE_ON




ORG h'00'
    
SAYAC  EQU h'20'
SAYAC1 EQU h'21'
SAYAC2 EQU h'22'
YON    EQU h'23'   

BANKSEL TRISB
CLRF TRISB
     
BANKSEL PORTB
CLRF PORTB
    
MOVLW h'01'
MOVWF SAYAC

CLRF YON          
    
BASLA
    MOVF SAYAC,W
    MOVWF PORTB
    
    CALL DELAY

  
    MOVF YON,W
    BTFSC STATUS,Z
    GOTO SOLA


SAGA_KAY
    BCF STATUS,C
    RRF SAYAC,F

    
    MOVF SAYAC,W
    XORLW b'00000001'
    BTFSS STATUS,Z
    GOTO BASLA

    CLRF YON     
    GOTO BASLA


SOLA
    BCF STATUS,C
    RLF SAYAC,F

    
    MOVF SAYAC,W
    XORLW b'10000000'
    BTFSS STATUS,Z
    GOTO BASLA

    MOVLW b'00000001'
    MOVWF YON    
    GOTO BASLA

DELAY
    MOVLW h'FF'
    MOVWF SAYAC1
    
DONGU1
    MOVLW h'FF'
    MOVWF SAYAC2
    
DONGU2
    DECFSZ SAYAC2,F
    GOTO DONGU2
    
    DECFSZ SAYAC1,F
    GOTO DONGU1
    
    RETURN

END
